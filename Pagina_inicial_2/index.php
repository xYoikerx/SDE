						<h1 class="titulo" >Violencia Simbolica</h1>
						<p class="parrafoVioPsicologica">
							Violencia simbólica es un concepto acuñado por el sociólogo francés Pierre Bourdieu. En las ciencias sociales se utiliza para describir una relación social asimétrica 
							donde el "dominador" ejerce violencia indirecta y no físicamente directa en contra de los "dominados", los cuales no la distinguen claramente o son inconscientes de 
							dichas prácticas en su contra, por lo cual son "cómplices de la dominación a la que están sometidos". Las prácticas de la violencia simbólica son caracterizados por 
							la reproducción de los roles sociales, estatus, género, posición social, categorías cognitivas, etc.  
						</p>
